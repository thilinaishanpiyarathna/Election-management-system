        public ArrayList<voter_reg> voterlist(){
        ArrayList<voter_reg> voterlist = new ArrayList<>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/slems", "", "");
            String query2 = "SELECT * from voter_reg";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query2);
            voter_reg voter_reg;
            while (rs.next()){
               voter_reg = new voter_reg(rs.getInt("voter_id"), rs.getString("full_name"), rs.getString("id_number"),rs.getString("address"),rs.getString("dob"),rs.getString("gender"));
               voterlist.add(voter_reg);
            }
        }
        catch(Exception g){
            JOptionPane.showMessageDialog(null, g);
        }
        return voterlist;
    }
    
    public void show_voter(){
        ArrayList<voter_reg> list = voterlist();
        DefaultTableModel model = (DefaultTableModel)voterlist_table.getModel();
        Object[] row = new Object[6];
        for(int i=0;i<list.size();i++){
            row[0] = list.get(i).getvoter_id();
            row[1] = list.get(i).getfull_name();
            row[2] = list.get(i).getid_number();
            row[3] = list.get(i).getaddress();
            row[4] = list.get(i).getdob();
            row[5] = list.get(i).getgender();
            model.addRow(row);
            
            
        }
    }

