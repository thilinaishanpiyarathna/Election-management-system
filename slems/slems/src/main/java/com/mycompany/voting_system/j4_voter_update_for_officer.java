    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/slems", "", "");
            int row = voterlist_table.getSelectedRow();
            String value = (voterlist_table.getModel().getValueAt(row, 0).toString());
            String query8 = "UPDATE voter_reg SET voter_id=?, full_name=?, id_number=?, address=?, dob=?, gender=? where voter_id=" + value;
            PreparedStatement pstd =conn.prepareStatement(query8);
            pstd.setString(1, vvoter_id.getText());
            pstd.setString(2, vfull_name.getText());
            pstd.setString(3, vid_num.getText());
            pstd.setString(4, vaddress.getText());
            pstd.setString(5, vdob.getText());
            if(vmale.isSelected()){
                gender="male";
            }
            if(vfemale.isSelected()){
                gender="female";
            }
            pstd.setString(6, gender);
            pstd.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) voterlist_table.getModel();
            model.setRowCount(0);
            show_voter();
            JOptionPane.showMessageDialog(null, "Updated Successfully!");
            }
        catch(Exception g){
            JOptionPane.showMessageDialog(null, g);
        }
    }//GEN-LAST:event_updateActionPerformed

