    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/slems", "", "");
            String query = "insert into candidate_reg (party_name, candidate_name, id_number, dob, gender, contact_no) values(?,?,?,?,?,?)";
            PreparedStatement pstd =conn.prepareStatement(query);
            pstd.setString(1, cparty_name.getText());
            pstd.setString(2, candidate_name.getText());
            pstd.setString(3, cid_number.getText());
            pstd.setString(4, cdob.getText());
            if(cmale.isSelected()){
                gender="male";
            }
            if(cfemale.isSelected()){
                gender="female";
            }
            pstd.setString(5, gender);
            pstd.setString(6, ccontact_no.getText());
            pstd.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) candidatelist_table.getModel();
            model.setRowCount(0);
            show_candidate();
            JOptionPane.showMessageDialog(null, "Inserted Successfully!");
        }
        catch(Exception g){
            JOptionPane.showMessageDialog(null, g);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

