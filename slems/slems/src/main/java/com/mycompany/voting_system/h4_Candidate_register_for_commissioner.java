    public ArrayList<candidate_reg> candidatelist(){
        ArrayList<candidate_reg> candidatelist = new ArrayList<>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/slems", "", "");
            String query5 = "SELECT * from candidate_reg";
            Statement sst = conn.createStatement();
            ResultSet rrs = sst.executeQuery(query5);
            candidate_reg candidate_reg;
            while (rrs.next()){
               candidate_reg = new candidate_reg(rrs.getInt("candidate_id"), rrs.getString("party_name"), rrs.getString("candidate_name"),rrs.getString("id_number"),rrs.getString("dob"),rrs.getString("gender"),rrs.getInt("contact_no"));
               candidatelist.add(candidate_reg);
            }
        }
        catch(Exception g){
            JOptionPane.showMessageDialog(null, g);
        }
        return candidatelist;
    }
    
    public void show_candidate(){
        ArrayList<candidate_reg> list = candidatelist();
        DefaultTableModel model = (DefaultTableModel)candidatelist_table.getModel();
        Object[] row = new Object[7];
        for(int i=0;i<list.size();i++){
            row[0] = list.get(i).getcandidate_id();
            row[1] = list.get(i).getparty_name();
            row[2] = list.get(i).getcandidate_name();
            row[3] = list.get(i).getid_number();
            row[4] = list.get(i).getdob();
            row[5] = list.get(i).getgender();
            row[6] = list.get(i).getcontact_no();
            model.addRow(row);
            
            
        }
    }

